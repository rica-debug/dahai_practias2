<script>
    function isEmpty(str) {
      return /^\s*$/.test(str);
    }
    function validateEmail(email) {
        var regex = /^[^\s@]+@[^\s@]+\.[a-zA-Z]{2,}$/; // 定义邮箱格式的正则表达式
        return regex.test(email); // 返回true或false来判断输入的邮箱是否符合要求
    }
    function error(name,msg="This field is required."){
        return '<label class="error" for="'+name+'">'+msg+'</label>'
    }
    $('.submit').click(function(){
        console.log(1)
        $('.error').remove();
        var company = $('.company').val();
        var firstname = $('.firstname').val();
        var lastname = $('.lastname').val();
        var address = $('.address').val();
        var zipcode = $('.zipcode').val();
        var city = $('.city').val();
        var country = $('.country').val();
        var phone = $('.phone').val();
        var email = $('.email').val();
        var message = $('.message').val();
        
        var is_ajax = true;
        if(isEmpty(company)){
            var error_msg = error('company');
            $('.company').parent('.item').append(error_msg);
            is_ajax = false;
        }
        if(isEmpty(firstname)){
            var error_msg = error('firstname');
            $('.firstname').parent('.item').append(error_msg);
            is_ajax = false;
        }
        if(isEmpty(lastname)){
            var error_msg = error('lastname');
            $('.lastname').parent('.item').append(error_msg);
            is_ajax = false;
        }
        if(isEmpty(address)){
            var error_msg = error('address');
            $('.address').parent('.item').append(error_msg);
            is_ajax = false;
        }
        if(isEmpty(zipcode)){
            var error_msg = error('zipcode');
            $('.zipcode').parent('.item').append(error_msg);
            is_ajax = false;
        }
        if(isEmpty(city)){
            var error_msg = error('city');
            $('.city').parent('.item').append(error_msg);
            is_ajax = false;
        }
        if(isEmpty(country)){
            var error_msg = error('country');
            $('.country').parent('.item').append(error_msg);
            is_ajax = false;
        }
        if(isEmpty(email)){
            var error_msg = error('email');
            $('.email').parent('.item').append(error_msg);
            is_ajax = false;
        }else{
            if(!validateEmail(email)){
                var error_msg = error('email','Please enter a valid email address');
                $('.email').parent('.item').append(error_msg);
                is_ajax = false;
            }
        }
        
        if(isEmpty(message)){
            var error_msg = error('message');
            $('.message').parent('.item').append(error_msg);
            is_ajax = false;
        }
        if(!$('.chose').hasClass('checked')){
            var error_msg = error('chose');
            error_msg = '<div class="chose-tips">'+error_msg+'</div>';
            $('.chose').parent('.agree').after(error_msg);
            is_ajax = false;
        }
        if(!is_ajax){
            return false;
        }
        $.ajax({
            type: "post",
            url: "/common/form.php",
            datatype : 'json',
            data : {
                "action":'message',
                "company":company,
                "firstname":firstname,
                "lastname":lastname,
                "address":address,
                "zipcode":zipcode,
                "phone":phone,
                "city":city,
                "country":country,
                "email":email,
                "message":message,
            },
            success:function(d) {
                $('.form inout').val('');
                alert('Message successful');
            }
        });
    });
    $('.search a').click(function(){
        var keyword = $('.search input').val();
        if(!isEmpty(keyword)){
            location.href = '/search-'+keyword+'.html';
        }
    })
</script>
